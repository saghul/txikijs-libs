const CHAR_W = 8;
const CHAR_H = 8;
const ATLAS_COLS = 16;
const ATLAS_ROWS = 6;
const ATLAS_W = ATLAS_COLS * CHAR_W;  // 128
const ATLAS_H = ATLAS_ROWS * CHAR_H;  // 48
const FIRST_CHAR = 32;

// Build RGBA pixel data for the font atlas from the 1-bit-per-pixel bitmap.
function buildFontAtlas() {
    const pixels = new Uint8Array(ATLAS_W * ATLAS_H * 4);

    for (let i = 0; i < 95; i++) {
        const col = i % ATLAS_COLS;
        const row = Math.floor(i / ATLAS_COLS);
        const baseX = col * CHAR_W;
        const baseY = row * CHAR_H;

        for (let y = 0; y < CHAR_H; y++) {
            const bits = FONT_DATA[i * 8 + y];

            for (let x = 0; x < CHAR_W; x++) {
                const on = (bits >> x) & 1;  // LSB = leftmost pixel
                const px = ((baseY + y) * ATLAS_W + (baseX + x)) * 4;

                pixels[px] = 255;
                pixels[px + 1] = 255;
                pixels[px + 2] = 255;
                pixels[px + 3] = on ? 255 : 0;
            }
        }
    }

    return pixels;
}


// --- Initialize ---

SDL.loadLibrary();
SDL.init();

const WIDTH = 640;
const HEIGHT = 480;

const window = new Window('txiki.js Pixel Font', WIDTH, HEIGHT);
const renderer = window.createRenderer();

// Create font atlas texture.
const fontTex = renderer.createTexture(
    PixelFormat.ABGR8888,
    TextureAccess.STATIC,
    ATLAS_W,
    ATLAS_H,
);

fontTex.update(buildFontAtlas(), ATLAS_W * 4);
fontTex.setBlendMode(BlendMode.BLEND);
fontTex.setScaleMode(ScaleMode.NEAREST);


// --- Text drawing helper ---

function drawText(text, x, y, scale = 2) {
    for (let i = 0; i < text.length; i++) {
        const code = text.charCodeAt(i) - FIRST_CHAR;

        if (code < 0 || code >= 95) {
            continue;
        }

        const sc = code % ATLAS_COLS;
        const sr = Math.floor(code / ATLAS_COLS);

        renderer.copy(
            fontTex,
            { x: sc * CHAR_W, y: sr * CHAR_H, w: CHAR_W, h: CHAR_H },
            { x: x + i * CHAR_W * scale, y, w: CHAR_W * scale, h: CHAR_H * scale },
        );
    }
}


// --- Main loop ---

let running = true;
let frame = 0;

while (running) {
    for (const event of SDL.pollEvents()) {
        if (event.type === EventType.QUIT || event.type === EventType.WINDOW_CLOSE_REQUESTED) {
            running = false;
        }

        if (event.type === EventType.KEY_DOWN && event.key === Key.ESCAPE) {
            running = false;
        }
    }

    if (!running) {
        break;
    }

    renderer.setDrawColor(20, 20, 35);
    renderer.clear();

    // Title
    fontTex.setColorMod(80, 200, 255);
    drawText('txiki.js Pixel Font', 60, 40, 3);

    // Subtitle
    fontTex.setColorMod(180, 180, 190);
    drawText('8x8 bitmap font from a Uint8Array', 60, 80, 2);

    // Show printable ASCII
    fontTex.setColorMod(255, 220, 100);
    drawText(' !"#$%&\'()*+,-./', 60, 130, 2);
    drawText('0123456789:;<=>?@', 60, 155, 2);
    drawText('ABCDEFGHIJKLMNOPQ', 60, 180, 2);
    drawText('RSTUVWXYZ[\\]^_`', 60, 205, 2);
    drawText('abcdefghijklmnopq', 60, 230, 2);
    drawText('rstuvwxyz{|}~', 60, 255, 2);

    // Animated rainbow text
    frame++;
    const hue = (frame * 3) % 360;
    const r = Math.floor(Math.sin(hue * Math.PI / 180) * 127 + 128);
    const g = Math.floor(Math.sin((hue + 120) * Math.PI / 180) * 127 + 128);
    const b = Math.floor(Math.sin((hue + 240) * Math.PI / 180) * 127 + 128);

    fontTex.setColorMod(r, g, b);

    const yBounce = Math.sin(frame * 0.05) * 10;

    drawText('Hello, World!', 120, 320 + yBounce, 4);

    // Footer
    fontTex.setColorMod(100, 100, 120);
    drawText('No TTF library needed!  ESC to quit.', 60, HEIGHT - 40, 1);

    renderer.present();
    SDL.delay(16);
}

// Cleanup
fontTex.destroy();
renderer.destroy();
window.destroy();
SDL.quit();
